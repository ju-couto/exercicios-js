class Calculadora {

    somar(number1, number2) {
        return number1 + number2;
    }
    subtrair(number1, number2) {
        return number1 - number2;
    }
    multiplicar(number1, number2) {
       return number1 * number2;
    }
    dividir(number1, number2) {
        return number1 / number2;
    }

}



module.exports = Calculadora